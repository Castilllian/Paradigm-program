def multiplication_table(n):
    for i in range(1, n + 1):
        for j in range(1, 10):  # Выводим таблицу умножения до 9
            print(f"{i} * {j} = {i * j}")

n = int(input("Введите число n: "))
multiplication_table(n)

##Этот скрипт использует вложенные циклы для создания таблицы умножения от 1 до n. 
##Внешний цикл перебирает числа от 1 до n, а внутренний цикл проходит от 1 до 9, 
##выводя на экран результат умножения.

##Выбор парадигмы обоснован тем, что процедурная парадигма подходит для данной задачи, 
##где основной упор делается на последовательное выполнение действий без необходимости 
##использования сложной логики или структурных изменений.